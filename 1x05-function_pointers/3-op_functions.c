#include "3-calc.h"

/**
 * op_add - Sum two params.
 *
 * @a: First param.
 * @b: Second param.
 * Return: int.
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Substract two params.
 *
 * @a: First param.
 * @b: Second param.
 * Return: int.
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiply two params.
 *
 * @a: First param.
 * @b: Second param.
 * Return: int.
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Split two params.
 *
 * @a: First param.
 * @b: Second param.
 * Return: int.
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Module of two params.
 *
 * @a: First param.
 * @b: Second param.
 * Return: int.
*/

int op_mod(int a, int b)
{
	return (a % b);
}

